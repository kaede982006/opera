function loadLyrics(tab, titleElement, contentElement) {
  titleElement.textContent = tab.dataset.title;
  contentElement.textContent = lyrics[tab.dataset.lyricsKey] || '';
}

function initLyricsTabs() {
  const tabs = Array.from(document.querySelectorAll('.tab[data-lyrics-key]'));
  const titleElement = document.querySelector('#lyrics-panel-title');
  const contentElement = document.querySelector('#lyrics-content');

  if (!tabs.length || !titleElement || !contentElement) {
    return;
  }

  tabs.forEach((tab) => {
    tab.addEventListener('click', () => {
      tabs.forEach((item) => {
        const selected = item === tab;
        item.classList.toggle('is-active', selected);
        item.setAttribute('aria-selected', String(selected));
      });

      loadLyrics(tab, titleElement, contentElement);
    });
  });

  loadLyrics(tabs[0], titleElement, contentElement);
}

window.initLyricsTabs = initLyricsTabs;
